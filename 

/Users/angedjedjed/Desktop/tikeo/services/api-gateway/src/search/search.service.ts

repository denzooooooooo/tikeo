
import { Injectable } from '@nestjs/common';
import { PrismaService } from '../prisma/prisma.service';

@Injectable()
export class SearchService {
  constructor(private prisma: PrismaService) {}

  async search(query: string, type?: string, page = 1, limit = 20) {
    const skip = (page - 1) * limit;

    if (type === 'events') {
      const events = await this.searchEvents(query, page, limit);
      return { ...events, organizers: [], users: [] };
    }

    if (type === 'organizers') {
      const organizers = await this.searchOrganizers(query, page, limit);
      return { events: [], ...organizers, users: [] };
    }

    // Search all
    const [events, organizers] = await Promise.all([
      this.searchEvents(query, page, Math.floor(limit / 2)),
      this.searchOrganizers(query, page, Math.floor(limit / 2)),
    ]);

    return {
      events: events.events,
      organizers: organizers.organizers,
      users: [],
      total: events.total + organizers.total,
    };
  }

  async searchEvents(query: string, page = 1, limit = 20) {
    const skip = (page - 1) * limit;

    const [events, total] = await Promise.all([
      this.prisma.event.findMany({
        where: {
          OR: [
            { title: { contains: query, mode: 'insensitive' } },
            { description: { contains: query, mode: 'insensitive' } },
            { venueName: { contains: query, mode: 'insensitive' } },
            { venueCity: { contains: query, mode: 'insensitive' } },
          ],
          status: 'PUBLISHED',
        },
        include: {
          organizer: {
            select: { id: true, companyName: true, logo: true },
          },
        },
        orderBy: { startDate: 'asc' },
        skip,
        take: limit,
      }),
      this.prisma.event.count({
        where: {
          OR: [
            { title: { contains: query, mode: 'insensitive' } },
            { description: { contains: query, mode: 'insensitive' } },
            { venueName: { contains: query, mode: 'insensitive' } },
            { venueCity: { contains: query, mode: 'insensitive' } },
          ],
          status: 'PUBLISHED',
        },
      }),
    ]);

    return { events, total, page, totalPages: Math.ceil(total / limit) };
  }

  async searchOrganizers(query: string, page = 1, limit = 20) {
    const skip = (page - 1) * limit;

    const [organizers, total] = await Promise.all([
      this.prisma.organizer.findMany({
        where: {
          OR: [
            { companyName: { contains: query, mode: 'insensitive' } },
            { description: { contains: query, mode: 'insensitive' } },
          ],
        },
        select: {
          id: true,
          companyName: true,
          logo: true,
          description: true,
          rating: true,
          totalEvents: true,
        },
        orderBy: { rating: 'desc' },
        skip,
        take: limit,
      }),
      this.prisma.organizer.count({
        where: {
          OR: [
            { companyName: { contains: query, mode: 'insensitive' } },
            { description: { contains: query, mode: 'insensitive' } },
          ],
        },
      }),
    ]);

    return { organizers, total, page, totalPages: Math.ceil(total / limit) };
  }

  async getTrendingSearches() {
    // Return some popular events as trending
    const trendingEvents = await this.prisma.event.findMany({
      where: { status: 'PUBLISHED', startDate: { gte: new Date() } },
      include: {
        organizer: {
          select: { id: true, companyName: true },
        },
      },
      orderBy: { views: 'desc' },
      take: 10,
    });

    return {
      events: trendingEvents,
      categories: ['Musique', 'Sport', 'Art', 'Technologie', 'Food'],
    };
  }
}


