
import { Controller, Get, Query, UseGuards } from '@nestjs/common';
import { SearchService } from './search.service';
import { JwtAuthGuard } from '../auth/guards/jwt-auth.guard';

@Controller('search')
export class SearchController {
  constructor(private readonly searchService: SearchService) {}

  @Get()
  async search(
    @Query('q') query: string,
    @Query('type') type?: string,
    @Query('page') page?: number,
    @Query('limit') limit?: number,
  ) {
    if (!query) {
      return { events: [], organizers: [], users: [], total: 0 };
    }

    return this.searchService.search(query, type, page || 1, limit || 20);
  }

  @Get('events')
  async searchEvents(
    @Query('q') query: string,
    @Query('page') page?: number,
    @Query('limit') limit?: number,
  ) {
    if (!query) {
      return { events: [], total: 0 };
    }

    return this.searchService.searchEvents(query, page || 1, limit || 20);
  }

  @Get('organizers')
  async searchOrganizers(
    @Query('q') query: string,
    @Query('page') page?: number,
    @Query('limit') limit?: number,
  ) {
    if (!query) {
      return { organizers: [], total: 0 };
    }

    return this.searchService.searchOrganizers(query, page || 1, limit || 20);
  }

  @Get('trending')
  async getTrending() {
    return this.searchService.getTrendingSearches();
  }
}


